#ifndef PRIME_H
#define PRIME_H

/**
 * @file prime.h
 * @brief Vérifie si un nombre est premier.
 *
 * Cette fonction prend un entier en entrée et retourne un booléen indiquant
 * si le nombre est premier ou non.
 *
 * @param a Le nombre à vérifier.
 * @return true Si le nombre est premier.
 * @return false Si le nombre n'est pas premier.
 */

bool isPrime(int a);

#endif // PRIME_H

